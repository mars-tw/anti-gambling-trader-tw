"""投資詐騙特徵知識庫。

每一個 ScamPattern 描述一種常見的投資詐騙手法:它長什麼樣、為什麼是詐騙、
以及一句能戳破它的「反詐金句」。這份知識庫同時被 checklist(自我檢測)、
report(分析報告警語)與 docs/anti-scam.md 共用,維持單一事實來源。
"""

from __future__ import annotations

from dataclasses import dataclass, field


@dataclass
class ScamPattern:
    """一種投資詐騙的特徵。"""

    code: str
    name: str                       # 詐騙型態名稱
    aka: list[str] = field(default_factory=list)   # 別稱 / 關鍵字
    red_flags: list[str] = field(default_factory=list)   # 辨識特徵
    why_scam: str = ""              # 為什麼這是詐騙(原理)
    rebuttal: str = ""              # 反詐金句:用一句話戳破它


# ── 假飆股群 / 假 VIP 群 / 假二群 ────────────────────────────
_FAKE_GROUP = ScamPattern(
    code="fake_stock_group",
    name="假飆股群 / 假 VIP 群 / 假二群",
    aka=["老師帶單", "飆股群", "VIP 群", "二群", "私帶", "報明牌", "內線消息",
         "免費進階群", "升級 VIP"],
    red_flags=[
        "有人主動把你拉進 LINE / Telegram 群,群裡『老師』每天報明牌。",
        "群裡有大量『學員』貼出獲利截圖、感謝老師,氣氛一片祥和。",
        "先給你幾支『會漲』的股票讓你嘗到甜頭,再叫你『升級 VIP / 進二群』付費。",
        "要你下載指定 App、加指定客服、或匯款到『老師合作的平台』。",
        "群組禁止你公開質疑,提問會被踢或私訊『安撫』。",
    ],
    why_scam=(
        "那些『學員』與獲利截圖多半是樁腳或假帳號(倖存者偏差的人造版)。"
        "老師同時對不同群報相反的方向,總有一群會猜中 — 猜中的那群被收割成付費會員,"
        "猜錯的被踢出。你看到的『神準』,是倖存者偏差被刻意製造出來的。"
    ),
    rebuttal=(
        "真的能穩定預測股市的人,會自己默默賺,不會免費拉你進群。"
        "把『老師』的歷史推薦全部記下來、扣掉看不到的失敗推薦,再用本工具算期望值 —— "
        "幾乎沒有例外,都是負的。"
    ),
)

# ── 保證獲利 / 高勝率話術 ───────────────────────────────────
_GUARANTEED = ScamPattern(
    code="guaranteed_return",
    name="保證獲利 / 高勝率話術",
    aka=["保證獲利", "穩賺不賠", "月配息", "保本", "勝率 90%", "穩定獲利",
         "包賺", "零風險", "一天賺", "翻倍"],
    red_flags=[
        "宣稱『保證獲利』『穩賺不賠』『零風險高報酬』。",
        "強調『勝率 90% 以上』,卻從不提『賠的時候賠多少』。",
        "用『月配 X%』『年化 OO%』包裝成像定存一樣安全。",
        "急著要你『現在不進場就錯過』,製造稀缺與急迫感。",
    ],
    why_scam=(
        "高報酬必然伴隨高風險,這是金融的鐵律 —— 任何宣稱『高報酬 + 零風險』的,"
        "不是騙就是龐氏。而『高勝率』本身會騙人:勝率 90% 但賺小賠大,"
        "一次大賠就回吐所有獲利,期望值仍是負的。"
    ),
    rebuttal=(
        "問一句:『如果穩賺,你為什麼要找我的錢,而不是去跟銀行無限借錢自己賺?』"
        "本工具用統計檢定就是要拆穿『高勝率 = 賺錢』的幻覺 —— 看的是期望值,不是勝率。"
    ),
)

# ── 假名師 / 假績效截圖 ─────────────────────────────────────
_FAKE_GURU = ScamPattern(
    code="fake_guru",
    name="假名師 / 假績效截圖",
    aka=["名師", "操盤手", "對帳單", "績效截圖", "獲利截圖", "百萬入帳",
         "學員見證", "素人翻身", "財富自由"],
    red_flags=[
        "炫耀豪車、名錶、出國,暗示『跟我就能財富自由』。",
        "貼出『對帳單 / 獲利截圖』,但截圖可以 P、可以只挑賺的那幾天。",
        "大量『學員見證』,卻沒有任何可驗證的長期、完整、第三方紀錄。",
        "課程 / 訂閱費很貴,且強調『名額有限』。",
    ],
    why_scam=(
        "截圖是『倖存者偏差』的完美載體:他只給你看賺的那幾筆,不給你看賠光退場的。"
        "就算對帳單是真的,單看幾天的暴賺也無法證明任何優勢 —— 那可能只是運氣。"
        "很多『名師』的真正收入來自學費與抽佣,不是交易本身。"
    ),
    rebuttal=(
        "要求看『完整、連續、夠長(數百筆以上)』的交易紀錄,而不是精選截圖。"
        "把那份紀錄丟進本工具,做顯著性檢定與樣本外驗證 —— 真本事過得了,運氣過不了。"
    ),
)

# ── 誊騙幣 / 假投資平台 ─────────────────────────────────────
_FAKE_PLATFORM = ScamPattern(
    code="fake_platform",
    name="誊騙幣 / 假投資平台",
    aka=["客服協助出金", "平台儲值", "USDT 入金", "刷流水", "解凍金",
         "交易所", "錢包", "穩定幣理財", "DeFi 高收益", "搬磚套利"],
    red_flags=[
        "叫你把錢匯到某個『平台 / 錢包 / 交易所』,而不是你自己掌控的帳戶。",
        "一開始能小額出金(誘餌),等你加碼後就『系統維護 / 要繳稅 / 要刷流水才能出金』。",
        "平台網站 / App 很像知名交易所,但網址、下載來源不對。",
        "宣稱『搬磚套利 / DeFi 高收益』穩賺,要你儲值參與。",
    ],
    why_scam=(
        "錢一旦進了『他們控制的平台』,帳面數字是他們隨便顯示的,你看到的『獲利』是假的。"
        "能小額出金只是釣餌,真正要你加碼後就用各種理由卡住出金 —— 這是典型的殺豬盤。"
    ),
    rebuttal=(
        "鐵律:只用『你自己掌控私鑰 / 自己開戶』的正規管道,絕不把錢匯到別人叫你匯的平台。"
        "任何『要先繳錢才能領回你的錢』的說法,100% 是詐騙。"
    ),
)


SCAM_PATTERNS: list[ScamPattern] = [
    _FAKE_GROUP,
    _GUARANTEED,
    _FAKE_GURU,
    _FAKE_PLATFORM,
]


def find_pattern(code: str) -> ScamPattern | None:
    for p in SCAM_PATTERNS:
        if p.code == code:
            return p
    return None
